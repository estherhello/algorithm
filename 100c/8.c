#include<stdio.h>

/*
	题目描述：
	要求输出国际象棋棋盘。
	程序分析：国际象棋棋盘由64个黑白相间的格子组成，分为8行*8列。用i控制行，j来控制列，根据i+j的和的	变化来控制输出黑方格，还是白方格，偶数为黑格，黑格用字符219表示黑色
*/

void main()
{
	int i,j,s;
	for(i = 1;i < 9;i++)
	{
		for(j = 1;j < 9;j++)
		{
			s = i+j;
			if(s%2 == 0)
			{
				printf("%c%c",219,219);
			}else {
				printf("  ");
			}
		}
		printf("\n");
	}

}